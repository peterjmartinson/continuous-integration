
def add(first_term, second_term):
    result = first_term + second_term
    return result


def subtract(first_term, second_term):
    result = first_term - second_term
    return result


def multiply(first_term, second_term):
    result = first_term * second_term
    return result


def divide(first_term, second_term):
    result = first_term / second_term
    return result
